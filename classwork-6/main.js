function countLetterA(str) {
  counter = 0;
  if (typeof(str) !== 'object') {
    for (i in str) {
      const elem = str[i];
      if (elem === 'a' || elem === 'A') {
        counter++;
      }
    }
  return counter;
  } else {
    for (i in str) {
      elem = str[i];
      for (j in elem) {
        letter = elem[j];
        if (letter === 'a' || letter === 'A') {
          counter++;
        }
      }
    }
    return counter
  }
}

